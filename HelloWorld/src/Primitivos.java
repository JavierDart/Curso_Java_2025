import java.util.Arrays;

public class Primitivos {
    public static void main(String[] args) {

        byte numByte = 127;
        System.out.println("numByte = " + numByte);
        System.out.println("Tipo byte en bytes es = " + Byte.BYTES);
        System.out.println("Tipo byte en bites es = " + Byte.SIZE);
        System.out.println("valor maximo de byte es = " + Byte.MAX_VALUE);
        System.out.println("valor mínimo de byte es = " + Byte.MIN_VALUE);

        short numShort = 127;
        System.out.println("numByte = " + numShort);
        System.out.println("Tipo byte en bytes es = " + Short.BYTES);
        System.out.println("Tipo byte en bites es = " + Short.SIZE);
        System.out.println("valor maximo de byte es = " + Short.MAX_VALUE);
        System.out.println("valor mínimo de byte es = " + Short.MIN_VALUE);
    }
}
